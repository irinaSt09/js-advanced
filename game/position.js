class Position {

    constructor(row, column) {
        this.row = row;
        this.column = column;
    }

    get row() {
        return this.row;
    }

    get column() {
        return this.column;
    }

    set row(row) {
        this.row = row;
    }

    set column(column) {
        this.column = column;
    }
}